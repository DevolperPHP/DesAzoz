function color(){
    var object = document.getElementById("text");
    object.style.color = "red";
}

function size(){
    var object = document.getElementById("text");
    object.style.fontSize = "40px";
}

function font(){
    var object = document.getElementById("text");
    object.style.fontFamily = "Impact,Charcoal,sans-serif";
}

function remove(){
    var object = document.getElementById("text");
    object.style.display = "none";
}